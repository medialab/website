			<p>
			Dominique Cardon est directeur du Médialab de Sciences Po. Il a été chercheur au Laboratoire des usages (SENSE) d’Orange Labs et professeur associé à l’Université Paris Est/LATTS. Ses travaux ont d’abord porté sur les formes ordinaires d’expression dans les médias traditionnels : émissions « Les auditeurs ont la parole » de RTL, messages d’indignation pour l’abbé Pierre sur France Inter, Menie Grégoire, Téléthon, etc. Il s’est ensuite consacré à l’étude des usages des technologies de communication. Il a ainsi travaillé sur la visiophonie, les outils coopératifs, les transformations des situations de travail sous l’effet des technologies numériques, le télétravail et les relations entre sociabilités et pratiques culturelles. Depuis le début des années 2000, ses recherches portent sur les usages d’internet dans différents contextes : la gouvernance de Wikipédia, la relation entre pratiques expressives et réseau social sur les blogs, la pudeur et l’impudeur en ligne, les pratiques des réseaux sociaux en ligne, etc. Son travail cherche à articuler une analyse des transformations de l’espace public et les dynamiques expressives et relationnelles. Il vise notamment à articuler les digital methods à un questionnement de sciences sociales sur les formes de l’engagement politique, les pratiques culturelles et la sociabilité. Depuis 2010, ses travaux proposent de conduire une analyse sociologique des algorithmes du web et des big data visant à comprendre à la fois la forme interne des calculs et le monde que les calculateurs projettent sur nos sociétés. Il a dirigé la publication des numéros spéciaux de la revue Réseaux sur « les réseaux sociaux de l’Internet » (n° 152, décembre 2008), le « Web 2.0 » (n° 154, mars 2009), « Politique des algorithmes » (n° 177, avril 2013) et « Méthodes digitales » (n° 188, 2014). Il a publié La démocratie Internet, Paris, Seuil/La République des idées, 2010, avec Fabien Granjon, Mediactivistes, Paris, Presses de Science po’, 2010 (2ème éd. enrichie : 2013), avec Antonio Casilli, Qu’est-ce que le digital labor ?, Paris, Ina Éditions, 2015 et A quoi rêvent les algorithmes. Nos vies à l’heure des big data, Paris, Seuil/République des idées, 2015. Il est membre du comité de rédaction de la revue Réseaux, du comité prospective de la CNIL et du conseil scientifique de Wikimédia France. Il a soutenu son habilitation à diriger des recherches en 2017.
				
